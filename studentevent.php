            <b> <h1 style="color:darkblue ;text-align:center;font-family:arial;font-size:30px"><u>Student Activities</u></h1></b>
	        <p>   Student activities or campus activities is a broad term describing most student-focused extracurricular clubs and programs at a university. Student activities are generally designed to allow students to get more involved and offer them opportunities in leadership, social responsibility, citizenship, volunteerism, and student employment. These activities are typically overseen by a Director of Student Activities, Student Affairs, or Student Engagement who often holds a master's degree in student development or a comparable field. Program directors often provide guidance for clubs, set standards for student run organizations, and can also help network with other institutions through organizations.</p>
	  
